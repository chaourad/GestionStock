    private void addbtnCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnCActionPerformed
        String cat = cattct.getText();
        if(cg.create(new Categories(cat) )){
            JOptionPane.showMessageDialog(this, "Bien ajouté");
            loadCateg();
        }
        
    }//GEN-LAST:event_addbtnCActionPerformed

    private void upbtnCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upbtnCActionPerformed
             String cat = cattct.getText();
             if(cg.update(new Categories(id, cat) )){
                JOptionPane.showMessageDialog(this, "Bien modifié");
                  loadCateg();
             }

    }//GEN-LAST:event_upbtnCActionPerformed

    private void delebtnCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delebtnCActionPerformed
       int reponse = JOptionPane.showConfirmDialog(this, "Voulez vous bien supprimer cette category ?");
        if (reponse == 0 && id != 0) {
            cg.delete(cg.findById(id));
               loadCateg();
        }    }//GEN-LAST:event_delebtnCActionPerformed

