    private void loadCateg(){
        model.setRowCount(0);
      for(Categories c : cg.findAll()){
            model.addRow(new Object[]{
               c.getId(),
                c.getNom()
              
            });
        }
        
    }
     private void loadRangement(){
        model2.setRowCount(0);
      for(Rangement c : rng.findAll()){
            model2.addRow(new Object[]{
               c.getId(),
                c.getNumero()
              
            });
        }
        
    }
    

