    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String desg = txtDesg.getText();
       Double prix = Double.parseDouble(txtprix.getText());
       if(pp.create(new Produit(desg,prix))){
           JOptionPane.showMessageDialog(this, "Bien ajout√©");
          loadProduit();
       }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblProduitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProduitMouseClicked
       id = Integer.parseInt(model.getValueAt(tblProduit.getSelectedRow(), 0).toString());
      
        txtDesg.setText(model.getValueAt(tblProduit.getSelectedRow(), 1).toString());
        txtprix.setText(model.getValueAt(tblProduit.getSelectedRow(), 2).toString());
    }//GEN-LAST:event_tblProduitMouseClicked

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
         String desg = txtDesg.getText();
       Double prix = Double.parseDouble(txtprix.getText());
       if(pp.update(new Produit(id,desg,prix))){
            JOptionPane.showMessageDialog(this, "Bien modifi√©");
            loadProduit();
       }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        if(id !=0){
            int reponse = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer cette salle ? ");
            if(reponse == 0){
                pp.delete(pp.findById(id));
                  loadProduit();
                JOptionPane.showMessageDialog(this, "Bien supprim√©");
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed


